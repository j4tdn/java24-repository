package loop;

public class Ex01For {
	
	public static void main(String[] args) {
		
		// Sử dụng for, in các số tự nhiên nhỏ hơn 10
		for(int i = 0; i < 10; ++i) {
			System.out.print(i +" ");
		}
		
		// Sử dụng for in các số tự nhiên từ 4-12
		System.out.println();
		
		for(int i = 4; i <=12; i++) {
			System.out.print(i+ "  ");
		}
	}

}
