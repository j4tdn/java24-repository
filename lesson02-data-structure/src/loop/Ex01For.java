package loop;

public class Ex01For {
	
	public static void main(String[] args) {
		
		// Sử dụng for, in các số nguyên dương < 10
		for(int i = 0; i < 10; i++) {
			System.out.print(i + " ");
		}
		
		System.out.println();
		
		// Sử dụng for, in các số nguyên dương từ 4 đến 12
		for(int i = 4; i < 12; i++) {
			System.out.print(i + " ");
		}
	}
	
}
