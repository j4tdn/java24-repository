package loop;

public class Ex01For {
	public static void main(String[] args) {

		// su dung for in cac so tu nhien duong < 10
		for (int i = 0; i < 10; i++) {
			System.out.print(i + " ");
		}
		System.out.println();
		// Su dung for, in cac so tu nhien duong tu 4 -> 12
		for (int i = 4; i <= 12; i++) {
			System.out.print(i + " ");
		}
	}
}
