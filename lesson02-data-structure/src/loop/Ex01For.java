package loop;

public class Ex01For {
	public static void main(String[] args) {
		
		// sử dụng for in các số nguyên dương < 10
		for (int i = 0; i < 10; i++) {
			System.out.print(i + " ");
		}
		System.out.println();

		// sử dụng for in các dố nguyên dương từ 4 - 12
		for (int i = 4; i < 13; i++) {
			System.out.print(i + " ");
		}
	}
}
