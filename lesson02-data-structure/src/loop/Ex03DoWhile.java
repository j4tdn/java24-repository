package loop;

public class Ex03DoWhile {
	
	public static void main(String[] args) {
		
		// do while ít nhất thực thi lệnh {} 1 lần
		// while nếu như mới vào đk sai --> không thực thi lần nào
		
		int i = 0;
		
		do {
			System.out.print(i + " ");
			i++;
		} while(i < 10);
		
		//1. Xem video CTDL mảng 
		//2. Xem video push code ở lớp + bài tập
		//3. Làm bài tập
	}

}
