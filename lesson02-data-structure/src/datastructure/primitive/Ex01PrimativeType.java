package datastructure.primitive;

public class Ex01PrimativeType {
	public static void main(String[] args) {
		int digit; // khai báo 1 biến tên digit có kiểm dữ liệu int
		digit = 5; // gán giá trị 5 cho biến digit
		System.out.println(digit);
		System.out.println("value --> " + digit);
		System.out.printf("value %d\n" , digit);
		
		
		//biến cục bộ : local variable
		char letter = 'A';
		float point = 5.7f; // số thực
		double score = 8.2d;
		boolean isValid = true;
		
		//
		System.out.println("letter :" + letter);
		System.out.println("point :" + point);
		System.out.println("score :" + score);
		System.out.println("isvalid :" + isValid);
		
		
		
	}

}