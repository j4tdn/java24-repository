package utils;

public class sortArray {
    public static void sortArrayByLevel(int[] array) {
        for (int i = 0; i < array.length - 1; i++) {
            for (int j = 0; j < array.length - 1 - i; j++) {
                if (calcLevel.calculateLevel(array[j]) > calcLevel.calculateLevel(array[j + 1])) {
                    int temp = array[j];
                    array[j] = array[j + 1];
                    array[j + 1] = temp;
                }
            }
        }
    }
}
