package loop;

public class Ex01For {

	public static void main(String[] args) {
		System.out.println("Cac so nguyen duong nho hon 10:");
		for (int i = 0; i < 10; i++) {
			System.out.print(i + " ");
		}
		
		System.out.println("\nCac so nguyen duong tu 4 den 12 la:");
		for (int i = 4; i < 12; i++) {
			System.out.print(i + " ");
		}
	}
	
}
